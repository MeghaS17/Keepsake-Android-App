package com.example.keepsake;

public class MyFirsts {

    public String bath;
    public String smile;
    public String word;
    public String teeth;
    public String outing;

    public MyFirsts(String bath, String smile, String word, String teeth, String outing) {
        this.bath = bath;
        this.smile = smile;
        this.word = word;
        this.teeth = teeth;
        this.outing = outing;
    }

    public MyFirsts() {
    }

    public String getBath() {
        return bath;
    }

    public void setBath(String bath) {
        this.bath = bath;
    }

    public String getSmile() {
        return smile;
    }

    public void setSmile(String smile) {
        this.smile = smile;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public String getTeeth() {
        return teeth;
    }

    public void setTeeth(String teeth) {
        this.teeth = teeth;
    }

    public String getOuting() {
        return outing;
    }

    public void setOuting(String outing) {
        this.outing = outing;
    }
}
